      subroutine optwat(nwav,wav,rindx,iindx)
c
c............ returns index of refraction for water:
c
      real nwvis(3,20),nwnir1(3,13),nwnir2(3,6)
      real nwir(3,16),nwnir3(3,24)
      real wav(1),rindx(1),iindx(1)
c
      data nwvis/ 
     $ .225,1.373,4.90e-8, .250,1.362,3.35e-8, .275,1.354,2.35e-8,
     $ .300,1.349,1.60e-8, .325,1.346,1.08e-8, .350,1.343,6.50e-9,
     $ .375,1.341,3.50e-9, .400,1.339,1.86e-9, .425,1.338,1.30e-9,
     $ .450,1.337,1.02e-9, .475,1.336,9.35e-10,.500,1.335,1.00e-9,
     $ .525,1.334,1.32e-9, .550,1.333,1.96e-9, .575,1.333,3.60e-9,
     $ .600,1.332,1.09e-8, .625,1.332,1.39e-8, .650,1.331,1.64e-8,
     $ .675,1.331,2.23e-8, .700,1.331,3.35e-8/
c
      data nwnir1/
     $ .725,1.330,9.15e-8, .750,1.330,1.56e-7, .775,1.330,1.48e-7,
     $ .800,1.329,1.25e-7, .825,1.329,1.82e-7, .850,1.329,2.93e-7,
     $ .875,1.328,3.91e-7, .900,1.328,4.86e-7, .925,1.328,1.06e-6,
     $ .950,1.327,2.93e-6, .975,1.327,3.48e-6,1.000,1.327,2.89e-6,
     $1.200,1.324,9.89e-6/
c
      data nwnir2/
     $1.400,1.321,1.38e-4,1.600,1.317,8.55e-5,1.800,1.312,1.15e-4,
     $2.000,1.306,1.10e-3,2.200,1.296,2.89e-4,2.400,1.279,9.56e-4/
c
      data nwnir3/
     $2.600,1.242,3.17e-3,2.650,1.219,6.70e-3,2.700,1.188,1.90e-2,
     $2.750,1.157,5.90e-2,2.800,1.142,1.15e-1,2.850,1.149,1.85e-1,
     $2.900,1.201,2.68e-1,2.950,1.292,2.98e-1,3.000,1.371,2.72e-1,
     $3.050,1.426,2.40e-1,3.100,1.467,1.92e-1,3.150,1.483,1.35e-1,
     $3.200,1.478,9.24e-2,3.250,1.467,6.10e-2,3.300,1.450,3.68e-2,

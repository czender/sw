     $3.500,1.400,9.40e-3,3.600,1.385,5.15e-3,3.700,1.374,3.60e-3,
     $3.800,1.364,3.40e-3,3.900,1.357,3.80e-3,4.000,1.351,4.60e-3/
c
      data nwir/
     $8.000,1.291,3.43e-2,8.200,1.286,3.51e-2,8.400,1.281,3.61e-2,
     $8.600,1.275,3.72e-2,8.800,1.269,3.85e-2,9.000,1.262,3.99e-2,
     $9.200,1.255,4.15e-2,9.400,1.247,4.33e-2,9.600,1.239,4.54e-2,
     $9.800,1.229,4.79e-2,10.00,1.218,5.08e-2,10.50,1.185,6.62e-2,
     $11.00,1.153,9.68e-2,11.50,1.126,1.42e-1,12.00,1.111,1.99e-1,
     $12.50,1.123,2.59e-1/
c
c
      do 10 n=1,20
        wav(n)   = nwvis(1,n)
        rindx(n) = nwvis(2,n)
        iindx(n) = nwvis(3,n)
   10 continue
c
      do 20 n=1,13
        m        = 20 + n
        wav(m)   = nwnir1(1,n)
        rindx(m) = nwnir1(2,n)
        iindx(m) = nwnir1(3,n)
   20 continue
c
      do 30 n=1,6
        m        = 33 + n
        wav(m)   = nwnir2(1,n)
        rindx(m) = nwnir2(2,n)
        iindx(m) = nwnir2(3,n)
   30 continue
c
      do 40 n=1,24
        m        = 39 + n
        wav(m)   = nwnir3(1,n)
        rindx(m) = nwnir3(2,n)
        iindx(m) = nwnir3(3,n)
   40 continue
c
      do 50 n=1,16
        m        = 63 + n
        wav(m)   = nwir(1,n)
